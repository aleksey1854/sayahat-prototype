"use client";

import { useEffect } from "react";

// Переход из админки по «Редактировать» — это server action с redirect(),
// а он в App Router прокрутку не сбрасывает. Админка длинная: человек
// стоит где-нибудь на сороковом магазине, жмёт «Редактировать» и попадает
// в кабинет на той же высоте, посреди формы. Выглядит так, будто страница
// не сменилась.
//
// Сбрасываем вручную и только для этого перехода. Внутри кабинета прокрутку,
// наоборот, надо сохранять: после сохранения товара форма должна остаться
// на месте, а не улетать наверх.
export function ScrollTopOnMount() {
  useEffect(() => {
    window.scrollTo(0, 0);
  }, []);
  return null;
}
